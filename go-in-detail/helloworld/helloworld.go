package main

import "fmt"

// 'import' is a keyword in Go 
// import in Go  == #include in C
// gives [package main] access to the functions from "fmt"
// import links 'fmt' package with package main
// 'fmt' == <stdio.h>
// fmt package contains functions for printing formatted output and scanning input
// fmt default Go standard library 
// fmt short for format

func main() {

// execution of program begins in func main() 
// between flower brackets is the body of program
// { body of program }

fmt.Println("Hello World")

}
